function val = xcotx(x)
    if abs(x) < 1e-5
        %Maclaurin series approximation near zero
        val = 1 - 1/3*x^2 - 1/45*x^4 - 2/945*x^6 - 1/4725*x^8 - 2/93555*x^10 - 1382/638512875*x^12 - 4/18243225*x^14 - 3617/162820783125*x^16 - 87734/38979295480125*x^18 - 349222/1531329465290625*x^20 - 310732/13447856940643125*x^22 - 472728182/201919571963756521875*x^24 - 2631724/11094481976030578125*x^26 - 13571120588/564653660170076273671875*x^28;
    else
        %Analytical value
        val = x*cot(x);
    end
end